""" additional module recommended by vscode """


def increment(param):
    """
    Increment parameter by 1 unit (to run test)
    """
    return param + 1


def decrement(param):
    """
    Decrement parameter by 1 unit (to run test)
    """
    return param - 1


def double(param):
    """
    Decrement parameter by 1 unit (to run test)
    """
    return param * 2
